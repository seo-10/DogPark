package com.dogpark.dto;

public class ShopDto {
	
	String pd_name;
	String pd_desc;
	int pd_price;
	int pd_stock;
	int pd_rate;
	
	public String getPd_name() {
		return pd_name;
	}
	public void setPd_name(String pd_name) {
		this.pd_name = pd_name;
	}
	public String getPd_desc() {
		return pd_desc;
	}
	public void setPd_desc(String pd_desc) {
		this.pd_desc = pd_desc;
	}
	public int getPd_price() {
		return pd_price;
	}
	public void setPd_price(int pd_price) {
		this.pd_price = pd_price;
	}
	public int getPd_stock() {
		return pd_stock;
	}
	public void setPd_stock(int pd_stock) {
		this.pd_stock = pd_stock;
	}
	public int getPd_rate() {
		return pd_rate;
	}
	public void setPd_rate(int pd_rate) {
		this.pd_rate = pd_rate;
	}
	
	
	
	
	
}
